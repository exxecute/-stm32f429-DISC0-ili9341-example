font* Font_GetFont(uint8_t Font) {
	switch(Font) {
	case FONT8:
		return &font8;

	case FONT12:
		return &font12;

	case FONT7:
		return &font7x5;

	default:
		return &font8;
	}
}

uint8_t Font_GetPage(uint8_t Font, uint8_t Letter, uint8_t height) {
	switch(Font) {
	case FONT8:
		return ascii_table_font8[(int)Letter - 32][height];

	case FONT12:
			return ascii_table_font12[(int)Letter - 32][height];

	default:
		return ascii_table_font8[(int)Letter - 32][height];
	}
}
